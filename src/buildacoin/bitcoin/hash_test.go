func testHasher(t *testing.T, hasher Hasher,
        inputString, expectedString string) {
    input, err := hex.DecodeString(inputString)
    if err != nil {
        t.Fatal(err.Error())
    }
    expected, err := HashFromHex(expectedString)
    if err != nil {
        t.Fatal(err.Error())
    }
    actual := hasher.Hash(input)
    if !expected.Equals(actual) {
        t.Fatal("hash mismatch: expected " + expected.String() + " / actual " +
                actual.String())
    }
}

