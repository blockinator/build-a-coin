func testMerkleRoot(t *testing.T, expectedString string,
        txHashStrings ...string) {
    expected, err := HashFromHex(expectedString)
    if err != nil {
        t.Fatal(err.Error())
    }
    hashes := make([]Hash, 0, 8)
    for _, str := range txHashStrings {
        hash, err := HashFromHex(str)
        if err != nil {
            t.Fatal(err.Error())
        }
        hashes = append(hashes, hash)
    }
    if len(hashes) < 1 {
        t.Fatal("no hashes?")
    }

    tree := MerkleTree(hashes, HashSha256d)

    actual:= tree[len(tree)-1]

    if !expected.Equals(actual) {
        for _, hash := range tree {
            println(hash.String())
        }
        t.Fatal("hash mismatch: expected " + expected.String() + " / actual " +
                actual.String())
    }
}

